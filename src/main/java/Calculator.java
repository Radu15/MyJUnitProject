public class Calculator {


    public int add(int x, int y) {
        return x + y;
    }

    public int diff(int x, int y) {
        return x - y;
    }

    public int multipy(int x, int y) {
        return x * y;
    }

    public int divide(int x, int y) {
        return x / y;
    }

    public int modulo(int x, int y) {
        return x % y;
    }

}
